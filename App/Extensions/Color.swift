import Foundation
import UIKit

extension UIColor {
    class func primary() -> UIColor {
        return UIColor.init(red: 40/255, green: 43/255, blue: 56/255, alpha: 1.0)
    }
    class func secondary() -> UIColor {
        return UIColor.init(red: 233/255, green: 76/255, blue: 68/255, alpha: 1.0)
    }
}
